async function fetchAccountData() {

	web3 = new Web3(provider);
	console.log("Web3 instance is", web3);
	const chainId = await web3.eth.getChainId();
	chainData = evmChains.getChain(chainId);
	const accounts = await web3.eth.getAccounts();
	console.log("Got accounts", accounts);
	selectedAccount = accounts[0]

	console.log(selectedAccount)
	const ethersProvider = new ethers.providers.Web3Provider(provider);
	const signer = ethersProvider.getSigner()
	const contract = new ethers.Contract(contractAddress, ABI, signer);


	if (chainId != 1) {
		document.getElementById("errormsg").innerHTML = "Switch to Mainnet"
		mintstatus = false
	} else {
		document.getElementById("amount").style.display = "block";
		document.getElementById("select-dropdown").style.display = "block"

		


		document.getElementById("errormsg").innerHTML = `Wallet: ${truncate(selectedAccount)}`
		document.getElementById("btn-connect").innerHTML = "Mint"
		document.getElementById("text2").innerHTML = ""
		amountChosen = document.getElementById("number").value

		document.getElementById("whitelistcheck").innerHTML = `Price: ${0.66 * amountChosen } ETH`

		document.getElementById("whitelistcheck").style.color = "black"


	}

	mintstatus = true




}


async function refreshAccountData() {
	await fetchAccountData(provider);
}

async function onConnect() {



	console.log("Opening a dialog", web3Modal);
	try {
		if (selectedAccount == null) {
			provider = await web3Modal.connect();

			document.querySelector("#btn-connect").addEventListener("click", mintAllowlist);


		} else {

			document.querySelector("#btn-connect").addEventListener("click", mintAllowlist);

		}

	} catch (e) {
		console.log("Could not get a wallet connection", e);
		return;
	}

	// Subscribe to accounts change
	provider.on("accountsChanged", (accounts) => {
		fetchAccountData();
	});

	// Subscribe to chainId change
	provider.on("chainChanged", (chainId) => {
		fetchAccountData();
	});

	// Subscribe to networkId change
	provider.on("networkChanged", (networkId) => {
		fetchAccountData();
	});

	await refreshAccountData();
}



async function onDisconnect() {

	console.log("Killing the wallet connection", provider);

	if (provider.close) {
		await provider.close();
		await web3Modal.clearCachedProvider();
		provider = null;
	}

	selectedAccount = null;

}

async function mintAllowlist() {
	amountChosen = document.getElementById("number").value


	if (mintstatus) {
		const ethersProvider = new ethers.providers.Web3Provider(provider);
		const signer = ethersProvider.getSigner()
		const contract = new ethers.Contract(contractAddress, ABI, signer);

		document.getElementById("btn-connect").innerHTML = "Minting..."

		const cost = await contract.cost()
		console.log(cost)
		
		total = ethers.utils.formatEther( cost ) * parseInt(amountChosen)
		console.log(ethers.utils.parseEther(total.toString()))

		let overrides = {
			value: ethers.utils.parseEther(total.toString())
		};
		try {

			let transaction = await contract.KickstarterMint(amountChosen, overrides)
			transaction.wait()
			document.getElementById("btn-connect").innerHTML = "Success"

		} catch (err) {


			console.log(err)

			document.getElementById("errormsg").innerHTML = "Failed, Try again..."
			document.getElementById("btn-connect").innerHTML = "Mint"

			if (err.code == "INSUFFICIENT_FUNDS") {
				document.getElementById("errormsg").innerHTML = "Not Enough Eth"


			} else if (err.message == "MetaMask Tx Signature: User denied transaction signature.") {
				document.getElementById("errormsg").innerHTML = "Metamask closed"



			}
		}
	}



}















